#include <reg52.h>
#include "rs232.h"

void comm_init();
void send1Byte (unsigned char value);
unsigned char read1Byte ();

RS232 rs232 = {
	comm_init,
	send1Byte,
	read1Byte
};

void comm_init() {
	PCON = 0x00; //使能波特率倍速位SMOD
	SCON = 0x50; //8位数据,可变波特率
	TMOD &= 0x0f; //清除定时器1模式位
	TMOD |= 0x20; //设定定时器1为8位自动重装方式
	TL1 = 0xFD; //设定定时初值
	TH1 = 0xFD; //设定定时器重装值
	ET1 = 0; //禁止定时器1中断
	TR1 = 1; //启动定时器1
	ES = 1;
	EA = 1;
}

void send1Byte (unsigned char value) {
	ES = 0;
	SBUF = value;
	while(!TI);
	TI = 0;	//记得要清零,不然会发送两次
	ES = 1;
}

unsigned char read1Byte () {
	unsigned char d;
	while(RI != 1);
	d = SBUF;
	RI = 0;
	return (d);
}